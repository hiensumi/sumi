vector<int> merge(vector <int> b, vector <int> c){
	vector <int> a;
	int n = b.size(), m = c.size(), i = 0, j = 0;
	while(i < n and j < m){
		if(b[i] < c[j]){
			a.pb(b[i]);
			i++;
		}
		else{
			a.pb(c[j]);
			j++;
		}
	}
	for(; i < n; i++) a.pb(b[i]);
	for(; j < m; j++) a.pb(c[j]);
	return a;
}
void build(int i, int l, int r){
	if(l == r){
		st[i].pb(a[l]);
		return;
	}
	int mid = l + r >> 1;
	build(2*i,l,mid); build(2*i+1,mid+1,r);
	st[i] = merge(st[2*i], st[2*i+1]);
}
